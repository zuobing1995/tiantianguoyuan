# 练习:
#   输入一个整数n,打印宽度为n个字符的正方形
#   如:
#     请输入: 4
#   打印:
#     ####
#     #  #
#     #  #
#     ####
#   如:
#     请输入: 6
#   打印:
#     ######
#     #    #
#     #    #
#     #    #
#     #    #
#     ######

n = int(input("请输入长方形宽度: "))

print("#" * n)  # 打印第一行
line = 1
# 打印中间的n-2行
while line <= (n - 2):
    # 此处打印中间的一行
    print('#' + ' ' * (n - 2) + '#')
    line += 1

if n != 1:
    print("#" * n)  # 打印最后一行
