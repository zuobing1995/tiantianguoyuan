# 2. 输入一个正整数(代表树干的高度)
# 　　　打印如下一棵圣诞树
#    如:
#      输入: 2 
#    打印:
#      *
#     ***
#      *
#      *
#    如:
#      输入: 3
#    打印:
#       *
#      ***
#     *****
#       *
#       *
#       *
n = int(input("请输入树干的高度: "))
# 打印树叶部分
for i in range(1, n + 1):
    stars = 2 * i - 1  # 星号个数
    blanks = n - i  # 空格数
    print(' ' * blanks + '*' * stars)


# 打印树干部分
for _ in range(n):
    print(' ' * (n - 1) + '*')


