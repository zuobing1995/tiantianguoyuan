# 2. 写程序求:
#   1 - 1/3 + 1/5 - 1/7 + 1/9 + .... + 1/(2*n-1)的和
#   1)　求当n等于10000时，此公式的和
#   2) 将上述结果乘以4后打印出来(看看这个数是什么？)

# 1 = +1/(2*1-1)

s = 0  # 用于累加
n = 1
sign = 1  # 正负号(符号)
while n <= 10000:
    s += sign * 1 / (2 * n - 1)
    n += 1
    sign *= -1  # 变换符号

print("和是:", s)

print('乘4之后的和是:', s * 4)
