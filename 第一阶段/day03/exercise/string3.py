# 练习:
#   输入一个字符串:
#     1. 判断您输入的字符串有几个空格
#     2. 将源字符串左右空白字符去掉,打印出有效的字符的个数
#     3. 判断您输入的是否是数字
#       3.1 如果是数字,判断您输入的数字是否大于100

s = input("请输入字符串: ")

print("空格的个数是:", s.count(' '))
print("去掉空白字符后:", s.strip())
if s.isdigit():
    print(s, '是数字')
    if int(s) > 100:
        print(s, '大于100')

